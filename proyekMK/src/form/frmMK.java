    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            st = cn.createStatement();
            if (txtMataKuliah.getText().equals("") ||
                    txtSks.getText().equals("") ||
                        txtDosen.getText().equals("") ||
                            txtKelas.getText().equals("") || 
                                txtHari.getText().equals("") ||
                                    txtJamMasuk.getText().equals("") ||
                                        txtJamSelesai.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Data tidak boleh kosong","Validasi data", JOptionPane.INFORMATION_MESSAGE);
                return;             
            }
            // aksi simpan data
            if (btnSimpan.getText() == "Simpan"){
                String cek = "SELECT * FROM jadwal WHERE matkul = '" + txtMataKuliah.getText() + "'";
                rs = st.executeQuery(cek);
                if (rs.next()){
                    JOptionPane.showMessageDialog(null, "Data sudah ada");
                }
                else{
                    String sql = "INSERT INTO jadwal VALUES ('" + txtHari.getText() + "','" + txtJamMasuk.getText() +
                            "','" + txtJamSelesai.getText() +
                            "','" + txtMataKuliah.getText() +
                            "','" + txtSks.getText() +
                            "','" + txtDosen.getText() +
                            "','" + txtKelas.getText() + "')";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                    Bersih();
                    TampilData();
                }
            }else{
                // aksi ubah data
                    String update = "UPDATE jadwal SET sks = '" + txtSks.getText() +
                            "', hari = '" + txtHari.getText() +
                            "', jam_msk = '" + txtJamMasuk.getText() +
                            "', jam_plg = '" + txtJamSelesai.getText() +
                            "', kelas = '" + txtKelas.getText() +
                            "', dosen ='" + txtDosen.getText() + 
                            "' WHERE matkul = '" +txtMataKuliah.getText() + "'";
                    st.executeUpdate(update);
                    JOptionPane.showMessageDialog(null,"Data berhasil disimpan");
                    Bersih();
                    TampilData();     
            }    
        } catch (Exception e){
            JOptionPane.showMessageDialog(null,e);   
        }
            
    }//GEN-LAST:event_btnSimpanActionPerformed

