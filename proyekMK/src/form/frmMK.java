    private void TampilData(){
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM jadwal");
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No");
            model.addColumn("Mata Kuliah");
            model.addColumn("SKS");
            model.addColumn("Hari");
            model.addColumn("Jam Masuk");
            model.addColumn("Jam Keluar");
            model.addColumn("Dosen");
            model.addColumn("Kelas");
            
            int no = 1;
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while(rs.next()){
                Object[] data = {
                    no++,
                    rs.getString("matkul"),
                    rs.getString("sks"),
                    rs.getString("hari"),
                    rs.getString("jam_msk"),
                    rs.getString("jam_plg"),
                    rs.getString("dosen"),
                    rs.getString("kelas")
                };
                model.addRow(data);
                tblData.setModel(model);
            }
            
        } catch (Exception e) {
        }
    }

